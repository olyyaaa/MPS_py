def align_text(art, alignment, width):
    if alignment == 'left':
        return [line.ljust(width) for line in art]
    elif alignment == 'center':
        return [line.center(width) for line in art]
    elif alignment == 'right':
        return [line.rjust(width) for line in art]
    return art

